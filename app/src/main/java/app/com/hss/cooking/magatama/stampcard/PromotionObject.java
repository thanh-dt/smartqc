package app.com.hss.cooking.magatama.stampcard;

public class PromotionObject {
	private String mId;
	private String mStampNeed;
	private String mUrlThumb;
	private String mContentPromot;
	private String mTitlePromot;
	private String mNumberStampHave;
	private int mStatusPromot;

	public String getId() {
		return mId;
	}

	public void setId(String mId) {
		this.mId = mId;
	}

	public String getStampNeed() {
		return mStampNeed;
	}

	public void setStampNeed(String mStampNeed) {
		this.mStampNeed = mStampNeed;
	}

	public String getUrlThumb() {
		return mUrlThumb;
	}

	public void setUrlThumb(String mUrlThumb) {
		this.mUrlThumb = mUrlThumb;
	}

	public String getContentPromot() {
		return mContentPromot;
	}

	public void setContentPromot(String mContentPromot) {
		this.mContentPromot = mContentPromot;
	}

	public String getTitlePromot() {
		return mTitlePromot;
	}

	public void setTitlePromot(String mTitlePromot) {
		this.mTitlePromot = mTitlePromot;
	}

	public String getNumberStampHave() {
		return mNumberStampHave;
	}

	public void setNumberStampHave(String mNumberStampHave) {
		this.mNumberStampHave = mNumberStampHave;
	}

	public int getStatusPromot() {
		return mStatusPromot;
	}

	public void setStatusPromot(int mStatusPromot) {
		this.mStatusPromot = mStatusPromot;
	}
	
	
}
